let current = localStorage.getItem("pi_lang") || "zh-HK";
if (!dict[current]) current = "zh-HK";

export function getLang() {
  return current;
}

export function t(key, fallback) {
  return dict[current]?.[key] ?? dict.en?.[key] ?? fallback ?? key;
}

export function setLang(lang) {
  if (!dict[lang]) return current;
  current = lang;
  localStorage.setItem("pi_lang", lang);
  document.documentElement.lang = lang;
  applyDom();
  return current;
}

export function applyDom(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = t(key);
    if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, val);
    } else if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  root.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"));
  });
}

export function leanWord(lean, home, away) {
  if (lean === "home") return home;
  if (lean === "away") return away;
  return t("lean_draw");
}
